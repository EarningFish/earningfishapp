part of 'forgot_password_screen_one_bloc.dart';

/// Abstract class for all events that can be dispatched from the
///ForgotPasswordScreenOne widget.
///
/// Events must be immutable and implement the [Equatable] interface.
class ForgotPasswordScreenOneEvent extends Equatable {
  @override
  List<Object?> get props => [];
}

/// Event that is dispatched when the ForgotPasswordScreenOne widget is first created.
class ForgotPasswordScreenOneInitialEvent extends ForgotPasswordScreenOneEvent {
  @override
  List<Object?> get props => [];
}
